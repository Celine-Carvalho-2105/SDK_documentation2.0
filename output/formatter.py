from datetime import datetime
from typing import Dict, Optional


# ---------------------------------------------------------------------------
# Markdown output
# ---------------------------------------------------------------------------

def format_markdown(sections: Dict[str, str], analysis: Dict) -> str:
    """Assemble all sections into a single Markdown document."""
    project_name = analysis.get("project_name", "Project Documentation")
    timestamp = datetime.now().strftime("%Y-%m-%d")

    parts = [
        f"<!-- Generated by Agentic Documentation Generator on {timestamp} -->",
        "",
    ]

    section_order = [
        "overview",
        "installation",
        "usage_guide",
        "api_docs",
        "architecture",
        "examples",
        "configuration",
        "contributing",
        "changelog",
    ]

    # Add sections in order
    for key in section_order:
        if key in sections and sections[key]:
            parts.append(sections[key])
            parts.append("")
            parts.append("---")
            parts.append("")

    # Add any extra sections not in the order list
    for key, content in sections.items():
        if key not in section_order and content:
            parts.append(content)
            parts.append("")

    parts.append(f"*Documentation generated on {timestamp}*")
    return "\n".join(parts)


# ---------------------------------------------------------------------------
# JSON output
# ---------------------------------------------------------------------------

def format_json(sections: Dict[str, str], analysis: Dict) -> str:
    """Format documentation as structured JSON."""
    output = {
        "metadata": {
            "project_name": analysis.get("project_name", "unknown"),
            "generated_at": datetime.now().isoformat(),
            "generator": "Agentic Documentation Generator",
            "language": analysis.get("language", "unknown"),
            "framework": analysis.get("framework", "unknown"),
            "architecture": analysis.get("architecture", "unknown"),
            "complexity": analysis.get("complexity", "unknown"),
            "total_files_analyzed": analysis.get("total_files", 0),
        },
        "analysis": {
            "description": analysis.get("description", ""),
            "features": analysis.get("features", []),
            "key_components": analysis.get("key_components", []),
            "dependencies": analysis.get("dependencies", []),
            "entry_points": analysis.get("entry_points", []),
        },
        "documentation": {
            key: content
            for key, content in sections.items()
            if content
        },
    }
    return json.dumps(output, indent=2, ensure_ascii=False)


