# ---------------------------------------------------------------------------
# Markdown → HTML conversion (simple, no deps)
# ---------------------------------------------------------------------------

def _markdown_to_html(md: str) -> str:
    """Convert Markdown to HTML for embedding in the doc page."""
    import re

    # Remove the generated comment if present
    md = re.sub(r"<!--.*?-->", "", md, flags=re.DOTALL)

    lines = md.split("\n")
    output = []
    i = 0
    in_code_block = False
    code_lang = ""
    code_lines = []

    while i < len(lines):
        line = lines[i]

        # Code block start/end
        if line.startswith("```"):
            if in_code_block:
                code_content = html_module.escape("\n".join(code_lines))
                output.append(f'<pre><code class="language-{code_lang}">{code_content}</code></pre>')
                in_code_block = False
                code_lines = []
                code_lang = ""
            else:
                in_code_block = True
                code_lang = line[3:].strip() or "text"
            i += 1
            continue

        if in_code_block:
            code_lines.append(line)
            i += 1
            continue

        # Headings
        if line.startswith("# "):
            output.append(f"<h1>{_inline(line[2:])}</h1>")
        elif line.startswith("## "):
            output.append(f"<h2>{_inline(line[3:])}</h2>")
        elif line.startswith("### "):
            output.append(f"<h3>{_inline(line[4:])}</h3>")
        elif line.startswith("#### "):
            output.append(f"<h4>{_inline(line[5:])}</h4>")
        # Horizontal rule
        elif re.match(r"^---+$|^\*\*\*+$", line.strip()):
            output.append("<hr>")
        # Blockquote
        elif line.startswith("> "):
            output.append(f"<blockquote><p>{_inline(line[2:])}</p></blockquote>")
        # Unordered list
        elif re.match(r"^[-*+] ", line):
            items = []
            while i < len(lines) and re.match(r"^[-*+] ", lines[i]):
                items.append(f"<li>{_inline(lines[i][2:])}</li>")
                i += 1
            output.append("<ul>" + "".join(items) + "</ul>")
            continue
        # Ordered list
        elif re.match(r"^\d+\. ", line):
            items = []
            while i < len(lines) and re.match(r"^\d+\. ", lines[i]):
                clean_line = re.sub(r'^\d+\. ', '', lines[i])
                items.append(f"<li>{_inline(clean_line)}</li>")
                i += 1
            output.append("<ol>" + "".join(items) + "</ol>")
            continue
        # Table
        elif "|" in line and i + 1 < len(lines) and re.match(r"^\|?[-| :]+\|?$", lines[i + 1].strip()):
            headers = [h.strip() for h in line.strip("|").split("|")]
            i += 2  # Skip separator
            rows = []
            while i < len(lines) and "|" in lines[i]:
                cells = [c.strip() for c in lines[i].strip("|").split("|")]
                rows.append(cells)
                i += 1
            thead = "<tr>" + "".join(f"<th>{_inline(h)}</th>" for h in headers) + "</tr>"
            tbody = "".join(
                "<tr>" + "".join(f"<td>{_inline(c)}</td>" for c in row) + "</tr>"
                for row in rows
            )
            output.append(f"<table><thead>{thead}</thead><tbody>{tbody}</tbody></table>")
            continue
        # Empty line
        elif line.strip() == "":
            output.append("")
        # Paragraph
        else:
            # Collect paragraph lines
            para_lines = []
            while i < len(lines) and lines[i].strip() and not lines[i].startswith(("#", ">", "-", "*", "+", "```", "---", "***")):
                if re.match(r"^\d+\. ", lines[i]):
                    break
                if "|" in lines[i]:
                    break
                para_lines.append(lines[i])
                i += 1
            if para_lines:
                output.append(f"<p>{_inline(' '.join(para_lines))}</p>")
            continue

        i += 1

    # Handle unclosed code block
    if in_code_block and code_lines:
        code_content = html_module.escape("\n".join(code_lines))
        output.append(f'<pre><code class="language-{code_lang}">{code_content}</code></pre>')

    return "\n".join(output)


def _inline(text: str) -> str:
    """Process inline Markdown: bold, italic, code, links."""
    import re

    text = html_module.escape(text)
    # Bold
    text = re.sub(r"\*\*(.+?)\*\*", r"<strong>\1</strong>", text)
    text = re.sub(r"__(.+?)__", r"<strong>\1</strong>", text)
    # Italic
    text = re.sub(r"\*(.+?)\*", r"<em>\1</em>", text)
    text = re.sub(r"_(.+?)_", r"<em>\1</em>", text)
    # Inline code
    text = re.sub(r"`(.+?)`", r"<code>\1</code>", text)
    # Links
    text = re.sub(r"\[(.+?)\]\((.+?)\)", r'<a href="\2">\1</a>', text)
    return text